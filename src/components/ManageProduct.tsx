"use client";
import React, { SVGProps, useEffect, useState } from "react";
import {
  Table,
  TableHeader,
  TableColumn,
  TableBody,
  TableRow,
  TableCell,
  Input,
  Button,
  DropdownTrigger,
  Dropdown,
  DropdownMenu,
  DropdownItem,
  Chip,
  User,
  Pagination,
  Selection,
  ChipProps,
  SortDescriptor,
} from "@heroui/react";
import dynamic from "next/dynamic";
import { deleteProduct, getAllProduct } from "@/Action/PaymentIntent";
import toast from "react-hot-toast";
import Link from "next/link";

type ProductProps = {
  name: string;
  id: string;
  description: string;
  price: number;
  brand: string;
  category: string;
  inStock: boolean;
  images?: {
    color: string;
    colorCode: string | null;
    image: string;
  }[];
}[];
export type IconSvgProps = SVGProps<SVGSVGElement> & {
  size?: number;
};

export function capitalize(s: string) {
  return s ? s.charAt(0).toUpperCase() + s.slice(1).toLowerCase() : "";
}

